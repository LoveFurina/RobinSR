pub async fn on_get_tutorial_cs_req(
    session: &mut PlayerSession,
    _body: &GetTutorialCsReq,
) -> Result<()> {
    session
        .send(
            CMD_GET_TUTORIAL_SC_RSP,
            GetTutorialScRsp {
                retcode: 0,
                tutorial_list: TUTORIAL_IDS
                    .iter()
                    .map(|id| Tutorial {
                        id: *id,
                        status: TutorialStatus::TutorialFinish.into(),
                    })
                    .collect(),
            },
        )
        .await
}

